import { toast } from "react-toastify"

export const shortSuccessMsg = (title) => {
  toast.success(title, {
    position: "top-right",
    autoClose: 1500,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
  })
}

